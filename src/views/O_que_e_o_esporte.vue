O exemplo aqui é um grupo de crianças que estão jogando no pátio da escola, no intervalo das aulas, podemos dizer que ali é apenas uma diversão entre alunos e colegas.
Já quando esse mesmo grupo de jovens, praticam sobre normas técnicas, podemos considerar uma atividade esportiva.
Mesmo assim, há situações em que mesmo o indivíduo executando prática esportiva, não é considerada o esporte, como em vários casos de paraquedismo, surf, escaladas.
Dito isso, entram em cena dois elementos chaves do esporte. A arbitragem e o público.
