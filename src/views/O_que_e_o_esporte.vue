    <h1>O que é o esporte</h1>
<p>
O esporte teve um grande crescimento no decorrer dos tempos, mesmo assim muitas pessoas costumam classificar  o esporte com lazer ou atividade física ou até  mesmo um ato de ganhar dinheiro.
Porém esse senso comum é muito superficial e não contempla a definição do esporte como um todo.</p>
