  A larva migrans (Ancylostoma caninum)  é uma infecção descrita como zoonose parasitária que envolve a migração de larvas. Pode existir em três formas, a ocular, a visceral e a mais comum a cutânea.Nesta a larva entra em contato com a pele, que migram para o tecido  subcutâneo, formando trajetos larvais na epiderme humana. Felizmente o ser humano é um hospedeiro acidental, sendo inofensivo.
