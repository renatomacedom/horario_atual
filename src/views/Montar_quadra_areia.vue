<h4>Preparação do terreno</h4>
<p>
Uma maneira econômica é fazer um recorte em uma área de grama ou de terra e jogar areia por cima.
Entretanto, a quadra não terá a profundidade ideal ou qualquer sistema de drenagem.
Dependendo do volume da chuva, formam-se poças de água que podem impedir seu uso.
O ideal é que a camada de areia possua 20 cm de altura e esteja nivelada ao chão.
Somando-se a camada ocupada pelo sistema de drenagem (de 30 a 40 centímetros) à de areia, a escavação para a construção de uma quadra terá profundidade de 50 a 60 centímetros.
</p>
<p>
Uma alternativa à escavação é a construção de uma quadra elevada, utilizando-se muretas de concreto ao redor da área para conter a areia.
 Entretanto, ainda que se construa um sistema de drenagem, a qualidade é comprometida, pois o solo é mais estável que a areia.
 Além disso, muretas podem apresentar um risco à segurança dos jogadores, já que o recuo da quadra não é muito grande e elas acabam ficando muito próximas à área de jogo.
</p>

<h4>Dreangem</h4>
<p>
O sistema de drenagem indicado para quadras de bareia é o mesmo utilizado nas de tênis: “espinha de peixe”.
 Um ramal principal, disposto no mesmo sentido das linhas laterais, mas no centro da quadra, capta a água que é conduzida por diversos ramais secundários.
  Assim como as costelas do peixe se ligam à vértebra, os ramais secundários são “cortados” perpendicularmente pelo ramal principal, que, por sua vez, escoa a água para a rede de drenagem ao qual está conectado.
  Tal sistema faz o escoamento da água por gravidade, ou seja, é necessário que haja um declive para que ela percorra o caminho traçado pelos ramais.
</p>
<p>
Os ramais são compostos por manta geotêxtil e duas camadas de brita (antes e depois da instalação dos tubos) e tubos perfurados.
A manta, que é permeável e atua como um filtro, também serve para proteger a estrutura, pois envolve completamente os ramais.
Após a instalação dos tubos e o preenchimento com a segunda camada de brita, a manta é fechada – cobrindo os ramais – e o terreno está pronto para receber areia.
</p>


<h4>Areia</h4>
<p>Os tipos de areia mais utilizados em quadras de beach tennis são: areia de lavagem de rio (a mesma usada pela construção civil) e areia tratada de dunas.
 Os principais aspectos a serem observados na escolha são a espessura do grão – quanto mais fino, mais confortável será para os jogadores – e os cuidados que serão necessários para higienizá-la.
 A areia de dunas impede a proliferação de fungos e bactérias, o que torna sua manutenção simples, em certos casos utilizando apenas um detergente neutro.
 Já a areia de lavagem requer higienização mais frequente e o uso de produtos químicos mais fortes, como o cloro.
 As instruções para manutenção e higienização variam de acordo com o tipo de areia adquirida e são disponibilizadas pelo próprio fornecedor.
 </p>

<p>
Para uma quadra de 11m x 21m e uma camada de 20 centímetros de areia serão necessários 46,2m³.
 O valor da areia, que pode ser calculado por metro cúbico ou por sacos, varia consideravelmente, não somente de acordo com o tipo escolhido, mas também com a região, já que o frete pode influenciar bastante no preço final.
 Além disso, o transporte da areia merece atenção, pois é fundamental garantir que ela não seja contaminada no trajeto.</p>

<h4>Postes, rede e linhas</h4>

<p>
A estrutura que sustenta os postes da rede é formada por um bloco de cimento que fica 30 centímetros abaixo do nível da quadra e possui uma bucha onde o poste é encaixado.
 O material mais indicado é o aço galvanizado, por ser mais resistente, e recomenda-se que o poste tenha regulagem de altura.
 Dessa maneira, a quadra pode ser utilizada tanto para o beach tennis como para o vôlei de praia, sem que haja necessidade de troca de rede – apenas de regulagem da altura.
  O par de postes pode custar de R$500,00 a R$900,00.
</p>
<p>
Kits de postes móveis, que podem ser facilmente transportados no porta-malas de um carro, são uma alternativa mais simples e prática à construção de uma quadra. Eles podem ser montados até mesmo na grama, caso não haja um espaço de areia disponível, e seu custo varia de R$250,00 a R$490,00.
</p>
<p>
O kit de linhas custa de R$100,00 a R$200,00 e existem várias marcas disponíveis no mercado. Ele é composto pelas quatro faixas (duas para as linhas laterais e duas para as de fundo) e seis pinos para fixação na areia. A montagem é simples, já que ilhoses indicam onde os pinos devem ser colocados, tanto nas extremidades das quatro linhas quanto no centro das duas linhas laterais (onde a quadra é dividida pela rede). Nas linhas do fundo também há ilhoses para demarcar a quadra para o jogo de simples (4,5m x 16m).
